package arrays;

public class Multiarray
{
	public static void main(String[] args)
	{
		
		String  sr1[][]={{"Narayan","Murti"},{"Ram","Swami"},{"Aroo","SwamI"}};
		for(int i=0;i<=2;i++)
		{
			for(int j=0;j<=1;j++)
			{
				System.out.print(sr1[i][j]+" ");
			}
			System.out.println();
		}
		
	}

}
